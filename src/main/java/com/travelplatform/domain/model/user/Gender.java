package com.travelplatform.domain.model.user;

/**
 * Enumeration of gender options for user profiles.
 */
public enum Gender {
    /**
     * Male gender.
     */
    MALE,

    /**
     * Female gender.
     */
    FEMALE,

    /**
     * Other gender identity.
     */
    OTHER,

    /**
     * User prefers not to specify gender.
     */
    PREFER_NOT_TO_SAY
}
