package com.travelplatform.domain.enums;

/**
 * Enumeration of accommodation types available on the platform.
 */
public enum AccommodationType {
    /**
     * Budget-friendly shared accommodation with dormitory-style rooms.
     */
    HOSTEL,

    /**
     * Commercial establishment offering lodging and meals.
     */
    HOTEL,

    /**
     * Self-contained housing unit within a larger building.
     */
    APARTMENT,

    /**
     * Standalone residential building.
     */
    HOUSE,

    /**
     * Large, luxurious house with additional amenities.
     */
    VILLA,

    /**
     * Full-service resort with extensive facilities.
     */
    RESORT
}
