package com.travelplatform.domain.enums;

/**
 * Enumeration of user engagement types with reels.
 */
public enum EngagementType {
    /**
     * User has viewed the reel.
     */
    VIEW,

    /**
     * User has liked the reel.
     */
    LIKE,

    /**
     * User has shared the reel.
     */
    SHARE,

    /**
     * User has bookmarked/saved the reel.
     */
    BOOKMARK
}
